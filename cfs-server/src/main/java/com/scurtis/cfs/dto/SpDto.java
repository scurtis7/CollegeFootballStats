package com.scurtis.cfs.dto;

import lombok.Getter;
import lombok.Setter;

@Getter
@Setter
public class SpDto implements Comparable<SpDto> {

    private int year;
    private String team;
    private String conference;
    private double rating;
    private int ranking;
    private double secondOrderWins;
    private double sos;
    private int offenseRanking;
    private double offenseRating;
    private double offenseSuccess;
    private double offenseExplosiveness;
    private double offenseRushing;
    private double offensePassing;
    private double offenseStandardDowns;
    private double offensePassingDowns;
    private double offenseRunRate;
    private double offensePace;
    private int defenseRanking;
    private double defenseRating;
    private double defenseSuccess;
    private double defenseExplosiveness;
    private double defenseRushing;
    private double defensePassing;
    private double defenseStandardDowns;
    private double defensePassingDowns;
    private double havocTotal;
    private double havocFrontSeven;
    private double havocDb;

    @Override
    public int compareTo(SpDto other) {
        return Integer.compare(getRanking(), other.getRanking());
    }

}
